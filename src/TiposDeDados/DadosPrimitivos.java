package TiposDeDados;

public class DadosPrimitivos {
    public static void main(String[] args) {
        /*
         * Dados Primitivos = int, double, float, char, boolean, short.
         * Objetivo da aula =  Criar um Ninja
         * */
        int idade = 16; // Valor maximo: 2147483647
        double altura = 1.65;
        char inicial = 'N';
        boolean vivoOuMorto = true;
        Long saldoBnacadrio = 999999L; // valor maximo: 9,233,372,036,854,775,807
        System.out.println("Idade Ninja: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Inicial: " + inicial);
        System.out.println("Esta vivo: " + vivoOuMorto);
        System.out.println("Saldo Bancario: " + saldoBnacadrio);
    }

}
